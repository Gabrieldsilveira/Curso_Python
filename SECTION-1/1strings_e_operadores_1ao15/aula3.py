"""
Python = Linguaguem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
# Aspas simples
print('Gabriel Dutra')

#Aspas duplas
print("Gabriel Dutra")

#Escape \" ou \'(para não confundir o programa, ele ira pular o próximo caracter) EX:
print("Gabriel \"Dutra\"")

# r (utilizado para mostrar o caracter de escape)
print(r"Gabriel \"Dutra\"")

# PARA DEIXAR O CÓDIGO MAIS LIMPO - SÓ INVERTER AS ASPAS!!
print("Gabriel 'Dutra'")