#Tipo de dado bool (boolean / booleano)
# Ao questionar algo em um programa só existem duas respostas SIM (TRUE) / NÃO (FALSE).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro.
print(10 == 10) #Sim -> True (verdadeiro)
print(10 == 11) #Não -> False (falso)
print(type(10 == 10))
print(type(10 == 11))
print(type(True))
print(type(False))

print(10 == 'oi')