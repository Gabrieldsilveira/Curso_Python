# TIPOS int e float
# int -> número inteiro
# O tipo int representa qualquer número, positivo ou negativo
# Sem sinal é considerado positivo.
print(11)
print(-11)
print(0)

# float -> número com ponto flutuante.
# o tipo float representa qualquer número, positivo ou negativo
# float sem sinal é considerado positivo

print(1.1, 10.11) #Na programação sempre usar o . para separar as casas decimais.
print(0.0, -1.2)

# A função (classe) type mostra o tipo que o python inferiu ao valor.
print(type('Gabriel'))
print(type(1), type(-1))
print(type(1.1), type(-1.1))