# OPERADORES ARITMÉTICOS #
adicao = 10 + 10
print('adição:', adicao)

subtracao = 10 - 5
print('subtração:', subtracao)

multiplicacao = 10 * 10
print('multiplicação:', multiplicacao)

divisao = 10 / 2.2 # SEMPRE RETORNA FLOAT, INDEPENDENTE DE TER NA CONTA OU NÃO
